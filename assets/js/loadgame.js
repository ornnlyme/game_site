const init_game = function () {
    if (window.location.href.split('?').length < 2) {
        return;
    }
    game_id = window.location.href.split('?')[1]
    game = games.find(game => game.id === game_id);
    if (!game) {
        return;
    }

    document.getElementById("avif_game_preloader_image").srcset = game.avif_game_preloader_image
    document.getElementById("fallback_game_preloader_icon_image").src = game.fallback_game_preloader_icon_image
    document.getElementById("avif_icon_image").srcset = game.avif_icon_image
    document.getElementById("fallback_icon_image").src = game.fallback_icon_image
    document.getElementById("gameframe").setAttribute("data-src", game.game_url)
    document.getElementById("gameframe").setAttribute("src", game.game_url)
    gameNameClasses = document.getElementsByClassName("game-name")
    for (let gameNameClass of gameNameClasses) {
        gameNameClass.innerHTML = game.name
    }
}


